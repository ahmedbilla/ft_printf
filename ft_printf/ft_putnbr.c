#include "ft_printf.h"

void	ft_putnbr(int n, size_t *len)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648", len);
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-', len);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10, len);
	ft_putchar((n % 10) + '0', len);
}
